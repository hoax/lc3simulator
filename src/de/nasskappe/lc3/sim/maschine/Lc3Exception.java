package de.nasskappe.lc3.sim.maschine;

public class Lc3Exception extends Exception {

	private static final long serialVersionUID = 6889639217763377709L;

	public Lc3Exception() {
		super();
	}

	public Lc3Exception(String message, Throwable cause) {
		super(message, cause);
	}

	public Lc3Exception(String message) {
		super(message);
	}

	public Lc3Exception(Throwable cause) {
		super(cause);
	}

}
